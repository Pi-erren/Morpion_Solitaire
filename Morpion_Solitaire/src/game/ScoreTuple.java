package game;

public class ScoreTuple {
	
	 private Integer first;
	 private String second;

	 public ScoreTuple(Integer first, String second) {
		 this.first = first;
		 this.second = second;
	 }

	 public Integer getFirst() {
		 return first;
	 }

	 public String getSecond() {
	        return second;
	 }
	 
}
